import fs from 'node:fs';
import path from 'node:path';

const projectRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
const defaultSource = path.join(projectRoot, 'vendor', 'sitelen-emoji-truth', 'default-stable.v1.json');
const sourceArg = process.argv[2];
const sourcePath = sourceArg ? path.resolve(process.cwd(), sourceArg) : defaultSource;
const outputPath = path.join(projectRoot, 'src', 'generated', 'emojiMapping.generated.ts');

const punctMap = {
  _punct_period: '.',
  _punct_colon: ':'
};

function normalizeToken(token) {
  return String(token || '').trim().toLowerCase();
}

function normalize(profile, source) {
  const entries = profile?.entries && typeof profile.entries === 'object' ? profile.entries : {};
  const aliases = profile?.aliases && typeof profile.aliases === 'object' ? profile.aliases : {};

  const outEntries = [];
  const wordMap = {};
  const punctuationMap = {};

  for (const [rawToken, rawOutput] of Object.entries(entries)) {
    if (typeof rawOutput !== 'string' || !rawOutput.trim()) {
      continue;
    }

    const token = normalizeToken(rawToken);
    if (!token) {
      continue;
    }

    if (punctMap[token]) {
      punctuationMap[punctMap[token]] = rawOutput;
      continue;
    }

    wordMap[token] = rawOutput;
    outEntries.push({ token, output: rawOutput });
  }

  for (const [rawAlias, rawCanonical] of Object.entries(aliases)) {
    const alias = normalizeToken(rawAlias);
    const canonical = normalizeToken(rawCanonical);
    if (!alias || !canonical || !wordMap[canonical]) {
      continue;
    }

    wordMap[alias] = wordMap[canonical];
    const canonicalEntry = outEntries.find((entry) => entry.token === canonical);
    if (!canonicalEntry) {
      continue;
    }

    const set = new Set(canonicalEntry.aliases || []);
    set.add(alias);
    canonicalEntry.aliases = Array.from(set);
  }

  return {
    entries: outEntries.sort((a, b) => a.token.localeCompare(b.token)),
    wordMap,
    punctuationMap,
    metadata: {
      source,
      version: typeof profile?.version === 'string' ? profile.version : 'unknown',
      generatedAt: typeof profile?.generated_at === 'string' ? profile.generated_at : 'unknown'
    }
  };
}

function main() {
  const raw = fs.readFileSync(sourcePath, 'utf8');
  const parsed = JSON.parse(raw);
  const normalized = normalize(parsed, sourcePath);

  const contents = `/* eslint-disable */\n// Auto-generated by scripts/update-emoji-mapping.mjs\n// Source: ${sourcePath}\n\nimport type { NormalizedEmojiMapping } from '../emoji/normalizeEmojiMapping';\n\nexport const GENERATED_EMOJI_MAPPING: Omit<NormalizedEmojiMapping, 'metadata'> = ${JSON.stringify(
    {
      entries: normalized.entries,
      wordMap: normalized.wordMap,
      punctuationMap: normalized.punctuationMap
    },
    null,
    2
  )};\n\nexport const GENERATED_EMOJI_MAPPING_METADATA: NormalizedEmojiMapping['metadata'] = ${JSON.stringify(
    normalized.metadata,
    null,
    2
  )};\n`;

  fs.writeFileSync(outputPath, contents, 'utf8');
  console.log(`Generated ${path.relative(projectRoot, outputPath)} from ${sourcePath}`);
}

main();
